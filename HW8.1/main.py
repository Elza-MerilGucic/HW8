mood = input("Please enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't be sad :)")
elif mood == "excited":
    print("It's great to see you excited!")
elif mood == "relaxed":
    print("It's great to be relaxed")
else:
    print("I don't recognise this mood")
